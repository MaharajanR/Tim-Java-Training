package com.maharajan;

public class Car {
    private char model;
    private double door;

    public void setModel(char model) {
        this.model = model;
    }

    public char getModel() {
        return this.model;
    }

    public void setDoor(double door) {
        this.door = door;
    }

    public double getDoor() {
        return this.door;
    }

}
