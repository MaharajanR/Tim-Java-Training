package com.maharajan;

public class Size {
    private int height;
    private int width;
    private int weight;

    public Size(int height, int width, int weight) {
        this.height = height;
        this.width = width;
        this.weight = weight;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }

    public int getWeight() {
        return weight;
    }
}
